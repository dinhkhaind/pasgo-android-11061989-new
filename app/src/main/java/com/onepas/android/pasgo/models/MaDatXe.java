package com.onepas.android.pasgo.models;

public class MaDatXe {
	private String tenMa;
	private String diemDen;
	private String hanSuDung;
	private String maID;
	private int loaiKhuyenMai;
	private int soTien;
	private boolean hieuLuc;
	private int tongSo;
	public String getTenMa() {
		return tenMa;
	}
	public void setTenMa(String tenMa) {
		this.tenMa = tenMa;
	}
	public String getDiemDen() {
		return diemDen;
	}
	public void setDiemDen(String diemDen) {
		this.diemDen = diemDen;
	}
	public String getHanSuDung() {
		return hanSuDung;
	}
	public void setHanSuDung(String hanSuDung) {
		this.hanSuDung = hanSuDung;
	}
	public String getMaID() {
		return maID;
	}
	public void setMaID(String maID) {
		this.maID = maID;
	}
	public int getLoaiKhuyenMai() {
		return loaiKhuyenMai;
	}
	public void setLoaiKhuyenMai(int loaiKhuyenMai) {
		this.loaiKhuyenMai = loaiKhuyenMai;
	}
	public int getSoTien() {
		return soTien;
	}
	public void setSoTien(int soTien) {
		this.soTien = soTien;
	}
	public boolean isHieuLuc() {
		return hieuLuc;
	}
	public void setHieuLuc(boolean hieuLuc) {
		this.hieuLuc = hieuLuc;
	}

	public int getTongSo() {
		return tongSo;
	}

	public void setTongSo(int tongSo) {
		this.tongSo = tongSo;
	}
}
