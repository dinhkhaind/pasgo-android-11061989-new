package com.onepas.android.pasgo.models;

public class MaDatCho {
	private String id;
	private String maTaiTro;
	private String giamGia;
	private String ngayBatDau;
	private String ngayKetThuc;
	private String nhaPhatHanh;
	private String quangCao;
	private int tongSo;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMaTaiTro() {
		return maTaiTro;
	}

	public void setMaTaiTro(String maTaiTro) {
		this.maTaiTro = maTaiTro;
	}

	public String getGiamGia() {
		return giamGia;
	}

	public void setGiamGia(String giamGia) {
		this.giamGia = giamGia;
	}

	public String getNgayBatDau() {
		return ngayBatDau;
	}

	public void setNgayBatDau(String ngayBatDau) {
		this.ngayBatDau = ngayBatDau;
	}

	public String getNgayKetThuc() {
		return ngayKetThuc;
	}

	public void setNgayKetThuc(String ngayKetThuc) {
		this.ngayKetThuc = ngayKetThuc;
	}

	public String getNhaPhatHanh() {
		return nhaPhatHanh;
	}

	public void setNhaPhatHanh(String nhaPhatHanh) {
		this.nhaPhatHanh = nhaPhatHanh;
	}

	public String getQuangCao() {
		return quangCao;
	}

	public void setQuangCao(String quangCao) {
		this.quangCao = quangCao;
	}

	public int getTongSo() {
		return tongSo;
	}

	public void setTongSo(int tongSo) {
		this.tongSo = tongSo;
	}
}
